/**
 * @file soma.h
 * @brief Arquivo de cabeçalho com as declarações das funções de soma e subtração.
 */

#ifndef SOMA_H_
#define SOMA_H_

/**
 * @brief Função que calcula a soma de dois números inteiros.
 *
 * Esta função recebe dois números inteiros e retorna a soma deles.
 *
 * @param va O primeiro número inteiro.
 * @param vb O segundo número inteiro.
 * @return A soma dos números va e vb.
 */
int soma2(int va, int vb);

/**
 * @brief Função que calcula a diferença entre dois números inteiros.
 *
 * Esta função recebe dois números inteiros e retorna a diferença entre eles.
 *
 * @param va O primeiro número inteiro (minuendo).
 * @param vb O segundo número inteiro (subtraendo).
 * @return A diferença entre os números va e vb.
 */
int menos(int va, int vb);

#endif /* SOMA_H_ */
